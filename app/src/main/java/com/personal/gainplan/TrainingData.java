    static Exercise ex(String name, String muscle, String target, int workSeconds, int restSeconds,
                       String setup, String movement, String mistakes, String progression, String... alternatives) {
        Exercise exercise = new Exercise();
        exercise.name = name;
        exercise.muscle = muscle;
        exercise.target = target;
        exercise.workSeconds = workSeconds;
        exercise.restSeconds = restSeconds;
        exercise.setup = setup;
        exercise.movement = movement;
        exercise.mistakes = mistakes;
        exercise.progression = progression;
        exercise.alternatives = Arrays.asList(alternatives);
        return exercise;
    }

    static String phaseForWeek(int week) {
        if (week <= 2) return "Вход в объем: 2-3 повтора в запасе";
        if (week <= 4) return "Набор повторений: добавляй 1-2 повтора";
        if (week <= 6) return "Набор нагрузки: добавляй вес или резинку";
        if (week == 7) return "Разгрузка: сократи подходы примерно на 40%";
        if (week <= 9) return "Новый цикл: верни объем и снова прогрессируй";
        if (week <= 11) return "Усложнение: темп, паузы, рюкзак";
        return "Контроль: вес, фото, объемы, максимум повторений";
    }

    static class DayPlan {
        final String day;
        final String title;
        final String focus;
        final List<Exercise> exercises;

        DayPlan(String day, String title, String focus, List<Exercise> exercises) {
            this.day = day;
            this.title = title;
            this.focus = focus;
            this.exercises = exercises;
        }

        boolean isWorkout() {
            return !exercises.isEmpty();
        }
    }

